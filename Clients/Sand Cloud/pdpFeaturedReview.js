var pdpFeaturedReviewInterval = setInterval(function () {
    var reviewStarDivs = document.querySelectorAll('.stamped-product-reviews-badge')
    var featuredReviewDivs = document.querySelectorAll('.featuredReviewDiv')
    if (!reviewStarDivs.length || featuredReviewDivs.length) {
        return
    }
    // do something with buttons
    console.log('inserting')
    var stampedWidget = document.querySelector('#stamped-main-widget')
    var sku = stampedWidget.getAttribute('data-product-sku')
    var review = jsonArray.find(thing => thing.sku == sku)
    var html = `<div class="stamped-review-body">
        <p class="stamped-review-content-body"><i>"${review.description}"</i> -${review.name}</p>
    </div>`

    var featuredReviewDiv = document.createElement('div')
    featuredReviewDiv.className = "featuredReviewDiv"
    featuredReviewDiv.innerHTML = html

    var url = new URL(window.location.href)
    var v2 = url.searchParams.get('v2')
    if (v2) {
        var addToCartButton = document.querySelector('[data-comp="StickyContainer"] [data-comp="AddToCart"]')
        addToCartButton.parentElement.insertBefore(featuredReviewDiv, addToCartButton)

    } else {
        reviewStarDivs[0].parentElement.insertBefore(featuredReviewDiv, reviewStarDivs[0])
        reviewStarDivs[1].parentElement.insertBefore(featuredReviewDiv.cloneNode(true), reviewStarDivs[1])
    }

}, 250)